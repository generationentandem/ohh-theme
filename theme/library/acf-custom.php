    }

    add_filter('acf/location/rule_values/category_slug', 'acf_location_rules_values_category_slug');

    function acf_location_rules_values_category_slug( $choices ) {
        $terms = acf_get_taxonomy_terms( 'category' );

        if( !empty($terms) ) {
            $choices = array_pop($terms);
        }

        return $choices;
    }

    add_filter('acf/location/rule_match/category_slug', 'acf_location_rules_match_category_slug', 10, 3);
    function acf_location_rules_match_category_slug( $match, $rule, $options )
    {
        $selected_category = get_category_by_slug(explode(':',$rule['value'])[1]);
        $current_category = $GLOBALS['tag_ID'];

        if($rule['operator'] == "==")
        {
            $match = ( $selected_category->cat_ID == $current_category );
        } elseif($rule['operator'] == "!=") {
            $match = ( $selected_category->cat_ID != $current_category );
        }

        return $match;
    }

